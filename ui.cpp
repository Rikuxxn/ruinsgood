//=============================================================================
// 仮面取得UIのコンストラクタ
//=============================================================================
CMaskUi::CMaskUi()
{
	// 値のクリア
	m_fTimer = 0.0f;// 経過時間(秒)
	m_fAlpha = 1.0f;// アルファ値
	m_bFading = false;// フェード開始フラグ
}
//=============================================================================
// 仮面取得UIのデストラクタ
//=============================================================================
CMaskUi::~CMaskUi()
{
	// なし
}
//=============================================================================
// 仮面取得UIの初期化処理
//=============================================================================
HRESULT CMaskUi::Init(void)
{
	// UIの初期化処理
	CUi::Init();

	return S_OK;
}
//=============================================================================
// 仮面取得UIの終了処理
//=============================================================================
void CMaskUi::Uninit(void)
{
	// UIの終了処理
	CUi::Uninit();
}
//=============================================================================
// 仮面取得UIの更新処理
//=============================================================================
void CMaskUi::Update(void)
{
	// UIの更新処理
	CUi::Update();

	// 時間経過
	m_fTimer++; // 毎フレームの経過時間を加算

	// 3秒経過でフェード開始
	if (m_fTimer >= 180.0f)
	{
		m_bFading = true;
	}

	// フェード処理（毎フレーム少しずつ減らす）
	if (m_bFading)
	{
		m_fAlpha -= 0.005f; // 1秒でアルファ0になる速さ

		if (m_fAlpha < 0.0f)
		{
			m_fAlpha = 0.0f;
		}
	}

	SetCol(D3DXCOLOR(1.0f, 1.0f, 1.0f, m_fAlpha));
}
//=============================================================================
// 仮面取得UIの描画処理
//=============================================================================
void CMaskUi::Draw(void)
{
	// UIの描画処理
	CUi::Draw();
}

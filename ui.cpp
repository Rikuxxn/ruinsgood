{
	m_showDeleyTime--;

	if (m_showDeleyTime <= 0)
	{
		m_isShow = true;
		m_fAlpha += 0.01f;

		if (m_fAlpha > 1.0f)
		{
			m_fAlpha = 1.0f;
		}

		// 通常に戻す
		SetCol(D3DXCOLOR(1.0f, 1.0f, 1.0f, m_fAlpha));

		bool n = m_isShow;

		if (n && !m_prevShow)
		{// 一回だけ通す
			// ランク表示サウンドの再生
			CManager::GetSound()->Play(CSound::SOUND_LABEL_RANK);
		}

		m_prevShow = n;
	}
	else
	{
		// 透明にする
		SetCol(D3DXCOLOR(1.0f, 1.0f, 1.0f, 0.0f));
	}

	// UIの更新処理
	CUi::Update();
}



//=============================================================================
// ステージ名表示のコンストラクタ
//=============================================================================
CStageUi::CStageUi()
{
	// 値のクリア
	m_fTimer = 0.0f;// 経過時間(秒)
	m_fAlpha = 0.0f;// アルファ値
	m_state = STATE_FADEIN; // 状態
}
//=============================================================================
// ステージ名表示のデストラクタ
//=============================================================================
CStageUi::~CStageUi()
{
	// なし
}
//=============================================================================
// ステージ名表示の初期化処理
//=============================================================================
HRESULT CStageUi::Init(void)
{
	// UIの初期化処理
	CUi::Init();

	return S_OK;
}
//=============================================================================
// ステージ名表示の更新処理
//=============================================================================
void CStageUi::Update(void)
{
	// UIの更新処理
	CUi::Update();

	// 時間経過
	m_fTimer++; // 毎フレームの経過時間を加算

	switch (m_state)
	{
	case STATE_FADEIN:
		m_fAlpha += 0.005f; // 1秒でアルファ0になる速さ

		if (m_fAlpha >= 1.0f)
		{
			m_fAlpha = 1.0f;
			m_fTimer = 0.0f;
			m_state = STATE_SHOW;
		}
		break;
	case STATE_SHOW:
		// 一定時間表示
		if (m_fTimer >= 180.0f)
		{
			m_state = STATE_FADEOUT;
		}
		break;
	case STATE_FADEOUT:
		m_fAlpha -= 0.005f; // 1秒でアルファ0になる速さ

		if (m_fAlpha <= 0.0f)
		{
			m_fAlpha = 0.0f;
			m_state = STATE_END;
		}
	case STATE_END:
		// 何もしない
		break;
	}

	SetCol(D3DXCOLOR(1.0f, 1.0f, 1.0f, m_fAlpha));
}



//=============================================================================
// 秘宝取得UIの更新処理
//=============================================================================
void CGetUi::Update(void)
{
	// UIの更新処理
	CUi::Update();

	// 時間経過
	m_fTimer++; // 毎フレームの経過時間を加算

	// 3秒経過でフェード開始
	if (m_fTimer >= 180.0f)
	{
		m_bFading = true;
	}

	// フェード処理（毎フレーム少しずつ減らす）
	if (m_bFading)
	{
		m_fAlpha -= 0.008f; // 1秒でアルファ0になる速さ

		if (m_fAlpha < 0.0f)
		{
			m_fAlpha = 0.0f;
		}
	}

	if (!m_isGet)
	{
		// 入手サウンドの再生
		CManager::GetSound()->Play(CSound::SOUND_LABEL_GET);

		m_isGet = true;
	}

	// 色の設定
	SetCol(D3DXCOLOR(1.0f, 1.0f, 1.0f, m_fAlpha));
}


